package project;
/**
 * Represents player positions in basketball, including hybrid roles,
 * with the player's primary position listed first and the secondary after.
 * For example, F_G stands for Forward-Guard.
 */
public enum Position {
    C,    // Center
    C_F,  // Center-Forward
    F,    // Forward
    F_C,  // Forward-Center
    F_G,  // Forward-Guard
    G,    // Guard
    G_F   // Guard-Forward
}
